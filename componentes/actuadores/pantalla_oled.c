void pantalla_principal(void* pv){

    i2c_pantalla_t* pantalla_t = (i2c_pantalla_t *) pv;

    u8g2_esp32_hal_t hal =  U8G2_ESP32_HAL_DEFAULT;
    hal.bus.i2c.scl = pantalla_t->SCK;
    hal.bus.i2c.sda = pantalla_t->SDA;

    u8g2_esp32_hal_init(hal);

    u8g2_t u8g2_lib;

    u8g2_Setup_sh1106_i2c_128x64_noname_f(
    &u8g2_lib,U8G2_R0,u8g2_esp32_i2c_byte_cb,u8g2_esp32_gpio_and_delay_cb);
    u8g2_SetI2CAddress(&u8g2_lib.u8x8,ADDR_PANTALLA);

    u8g2_InitDisplay(&u8g2_lib);
    u8g2_SetPowerSave(&u8g2_lib,0);

   srand(time(NULL)); //Para generar nueva semilla de secuencias

    while(1){
        //caratula_turbidez(&u8g2_lib);
        //caratula_luz(&u8g2_lib); 
        if(xSemaphoreTake(btn_irq.binary_semaphore,0)==pdTRUE){
            //No se pone portMAX_DELAy para que no se congele, por eso se pone 0
            contador_caratula++;
            if(contador_caratula >3) contador_caratula =1;
        }
        vTaskDelay(pdMS_TO_TICKS(200));

        //Logica caratulas y datos
        //Se actualiza cada ciclo, que es lo que buscamos
        switch (contador_caratula){
        case 1 :
            caratula_turbidez(&u8g2_lib);
            break;
        case 2:
            caratula_luz(&u8g2_lib);
            break;
        case 3:
            caratula_temperatura(&u8g2_lib);
            break;
        default:
            break;
        }
    }
}

void Init_pantalla(uint8_t sda,uint8_t sck,uint8_t btn_gpio){
    ESP_LOGI(TAG,"Panalla oled iniciada");

    btn_irq.btn_pin = btn_gpio;

    gpio_config_t btn_cfg ={
        .pin_bit_mask = (1ULL<<btn_gpio),
        .mode = GPIO_MODE_INPUT,
        .pull_down_en =GPIO_PULLDOWN_ENABLE,
        .pull_up_en = GPIO_PULLUP_DISABLE,
        .intr_type = GPIO_INTR_NEGEDGE,
    };
    gpio_config(&btn_cfg);
    gpio_install_isr_service(0);
    gpio_isr_handler_add(btn_irq.btn_pin,btn_interrupt,NULL);

    btn_irq.binary_semaphore = xSemaphoreCreateBinary();

    oled.SCK = sck;
    oled.SDA = sda;

    xTaskCreatePinnedToCore(pantalla_principal,"oled_pantalla",8192,&oled,1,NULL,0);
}